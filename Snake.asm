    mov grow_state,BIGGER
   ; [di]=last part ,[bx]=new part
    lea bx,snake
    mov ax,lent
    shl ax,2
    add bx,ax
    mov di,bx
    sub di,4
   ; new part's cords <-- last part's cords
    mov ax,ds:[di]
    mov ds:[bx],ax
   ; char and attribute
